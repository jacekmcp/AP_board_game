package sample.Controllers;

import java.io.Serializable;

public class RoomCoordinates implements Serializable {
    public RoomCoordinates(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    private double x,y;
}
